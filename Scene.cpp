#include "Scene.h"
#include "Edit.h"

//=============================================================================
// コンストラクタ
//=============================================================================
CScene::CScene(MODE mode):CObject(7)
{
	// 値のクリア
	m_mode = mode;
}
//=============================================================================
// デストラクタ
//=============================================================================
CScene::~CScene()
{
	// なし
}
//=============================================================================
// 生成処理
//=============================================================================
CScene* CScene::Create(MODE mode)
{
	CScene* pScene = nullptr;

	switch (mode)
	{
	case MODE_EDIT:
		// エディット画面の生成
		pScene = new CEdit();
		break;

	default:
		break;
	}

	// nullptrだったら
	if (pScene == nullptr)
	{
		return nullptr;
	}

	// 初期化失敗時
	if (FAILED(pScene->Init()))
	{
		return nullptr;
	}

	return pScene;
}